
public class Ex02_연산자 {

	public static void main(String[] args) {
		// Ctrl + Shift + f 정렬
		int num1 = 7;
		int num2 = 3;
		float num3 = (float)7.0;
		float num4 = 3.0f;
		
		System.out.println(num1/num2);
		System.out.println(num1%num2);
		System.out.println(num3/num4);
		System.out.println(num1/num4);
		
	}

}
