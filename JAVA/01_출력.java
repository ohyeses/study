
public class Ex01_출력 {

	public static void main(String[] args) {
		System.out.println("Hello World");
		System.out.println("안녕하세요");
		System.out.println(3+5);
		// 주석처리는 //
		// run 단축키는 Ctrl + f11
		
		//변수
		// 정수형 숫자 3을 a라는 변수에 대입하시오
		int a = 3;
		
		// a라는 변수를 출력하시오
		System.out.println(a);
		
		
		boolean check = true;
		System.out.println(check);
		
		
		
	}

}
